package pe.gob.pj.csjar.tickets.controlador;

import java.util.*;

public class TabItems {

	   private String idTabla;
	   private String idItem;
	   private String nomCorto;
	   private String nomLargo;
	   private Double prerelacion;
	   private int nivel;
	   private String idItemSup;
	   private String activo;
	   
	
	public String getIdTabla() {
		return idTabla;
	}
	public void setIdTabla(String idTabla) {
		this.idTabla = idTabla;
	}
	public String getIdItem() {
		return idItem;
	}
	public void setIdItem(String idItem) {
		this.idItem = idItem;
	}
	public String getNomCorto() {
		return nomCorto;
	}
	public void setNomCorto(String nomCorto) {
		this.nomCorto = nomCorto;
	}
	public String getNomLargo() {
		return nomLargo;
	}
	public void setNomLargo(String nomLargo) {
		this.nomLargo = nomLargo;
	}
	public Double getPrerelacion() {
		return prerelacion;
	}
	public void setPrerelacion(Double prerelacion) {
		this.prerelacion = prerelacion;
	}
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public String getIdItemSup() {
		return idItemSup;
	}
	public void setIdItemSup(String idItemSup) {
		this.idItemSup = idItemSup;
	}
	public String getActivo() {
		return activo;
	}
	public void setActivo(String activo) {
		this.activo = activo;
	}
	   

}
